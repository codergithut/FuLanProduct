package fulan.tianjian.demo.convert;

public class ConvertModel<S, T> {
	
	
	private S source;
	
	private T target;

	public S getSource() {
		return source;
	}

	public void setSource(S source) {
		this.source = source;
	}

	public T getTarget() {
		return target;
	}

	public void setTarget(T target) {
		this.target = target;
	}
	
	

}
